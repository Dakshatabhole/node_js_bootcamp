//WHILE LOOP
let counter =0;
while(counter <3){
    console.log(`Loop interation: ${counter}`);
    counter = counter + 1;
}

console.log("loop terminated!")

//FOR LOOP
const fruits = ["Apple", "Mango", "Watermelon"];
for(let i=0; i<fruits.length; i++){
    console.log(`fruit at index ${i} is ${fruits[i]}`);
}